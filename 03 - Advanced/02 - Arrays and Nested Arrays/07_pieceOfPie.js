function pieceOfPie(array, flavor1, flavor2) {
    return array.slice(array.indexOf(flavor1), array.indexOf(flavor2));
}

pieceOfPie(['Pumpkin Pie',
'Key Lime Pie',
'Cherry Pie',
'Lemon Meringue Pie',
'Sugar Cream Pie'],
'Key Lime Pie',
'Lemon Meringue Pie'
);

